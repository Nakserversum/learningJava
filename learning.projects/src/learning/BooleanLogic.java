package learning;

public class BooleanLogic {

    public static void main(String[] args) {
        boolean x = true;
        boolean y = false;

        System.out.println(x); //positiv operator
        System.out.println(!x); //negativ operator

        System.out.println(y); //positiv operator
        System.out.println(!y); //negativ operator

        System.out.println(x && y); // wahr und falsch = false
        System.out.println(x || y); // oder operator = true, ein wert muss true sein


    }
}
